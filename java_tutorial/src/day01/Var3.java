package day01;

public class Var3 {
	public static void main(String[] args) {
		// 변수로 이름, 생년월일, 키를 저장해놓고 사용
		String 이름 = "홍길동";
		String 생년월일 = "2001년 1월 15일";
		int 키 = 160;
		
		
		System.out.println("이름 : "+이름);
		System.out.println("생일 : "+생년월일);
		System.out.println("키 : "+키+"cm");
		/*
		   이름 : 홍길동
		   생일 : 2001년 1월 15일 
		   키 : 175.5cm
		   
		   단, 이름과 생년월일, 키는 변수로 저장해서 사용
		 */
	}


}
